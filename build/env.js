/**
 * Tamaño máximo permitido para cada intento del jugador.
 *
 * Esta constante define la longitud exacta que debe tener la palabra
 * objetivo y también cada intento del jugador en el juego Wordle.
 *
 * @constant
 * @type {number}
 */
export const MAX_WORD_SIZE = 5;
/**
 * Número máximo de intentos que el jugador tiene para adivinar la palabra.
 *
 * Cuando el número de turnos supera este valor, el juego termina con derrota.
 *
 * @constant
 * @type {number}
 */
export const MAX_ATTEMPTS = 6;
